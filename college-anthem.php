                   <p> he College Song
                    The ‘LaSallian Anthem’ was first introduced in the Silver Jubilee Magazine of the College in 1930. 
                    The lyrics of the College anthem were written by Rev.Bro.Marcus Felix  FSC  B.A.  It was Mr. S.Norbert Rodrigo A.V.C.M.  who composed the air/melody of it and arranged it with harmony. 
                    The ‘LaSallian Anthem’ is the song of aspiration of all LaSalliana. The message conveyed in the 4 stanzas calls all LaSallians to rally round the LaSallian flag. 
                    
                    The song tells us to be loyal to our God, our church and our country with the true LaSallian characteristics of keenness, harmlessness & loyal ness and all round capabilities.
                    
                    It also advises us to rejoice in victory, learn to take defeat & to always stand for our Alma Mater. 
                    
                    
